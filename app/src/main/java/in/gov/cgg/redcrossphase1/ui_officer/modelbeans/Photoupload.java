package in.gov.cgg.redcrossphase1.ui_officer.modelbeans;

public class Photoupload {

    private String rcDetailId;
    private String phoneNo;
    private String dateOfBirth;
    private String bloodGroup;
    private String email;
    private String photoUpload;

    public String getRcDetailId() {
        return rcDetailId;
    }

    public void setRcDetailId(String rcDetailId) {
        this.rcDetailId = rcDetailId;
    }

    public String getPhoneNo() {
        return phoneNo;
    }

    public void setPhoneNo(String phoneNo) {
        this.phoneNo = phoneNo;
    }

    public String getDateOfBirth() {
        return dateOfBirth;
    }

    public void setDateOfBirth(String dateOfBirth) {
        this.dateOfBirth = dateOfBirth;
    }

    public String getBloodGroup() {
        return bloodGroup;
    }

    public void setBloodGroup(String bloodGroup) {
        this.bloodGroup = bloodGroup;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getPhotoUpload() {
        return photoUpload;
    }

    public void setPhotoUpload(String photoUpload) {
        this.photoUpload = photoUpload;
    }
}
